package Enhancement_STFT.FourrierTransform;

public class complex{
    static class complex{
        float real;
        float img;

        complex( float r, float i){
            this.real = r;
            this.img = i;
        }

        public static module{
            float abs=Math.sqrt(Math.pow(this.real,2)+Math.pow(this.img,2));
            return abs;
        }
    }
}
